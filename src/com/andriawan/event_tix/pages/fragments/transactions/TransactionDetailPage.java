    /**
     * Creates new form TransactionDetail
     * @param transactionId
     * @param mainPanel
     */
    public TransactionDetailPage(int transactionId, JPanel mainPanel) {
        initComponents();
        
        this.mainPanel = mainPanel;
        this.transactionId = transactionId;
        repository = new TransactionRepository();
        transaction = repository.getTransaction(this.transactionId);
        if (transaction == null) {
            mainPanel.removeAll();
            mainPanel.add(new TransactionPage(mainPanel));
            mainPanel.repaint();
            mainPanel.revalidate();
        }

        generateQRCode();
    }
    
    private void generateQRCode() {
        try {
            ImageIcon barcodeImage = BarcodeGenerator.getBarcode("Transaction id is " + transaction.id);
            barcodeLabel.setIcon(barcodeImage);
        } catch (WriterException | IOException e) {
            JOptionPane.showMessageDialog(null, "Gagal memuat gambar");
            mainPanel.removeAll();
            mainPanel.add(new TransactionPage(mainPanel));
            mainPanel.repaint();
            mainPanel.revalidate();
        }
    }

